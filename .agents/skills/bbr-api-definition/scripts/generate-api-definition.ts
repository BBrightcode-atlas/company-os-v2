function countBy(values: string[]): Record<string, number> {
  return values.reduce<Record<string, number>>((counts, value) => {
    counts[value] = (counts[value] ?? 0) + 1;
    return counts;
  }, {});
}

function escapeCell(value: string): string {
  return String(value).replace(/\|/g, "\\|").replace(/\n/g, "<br>");
}

function isScalar(value: unknown): boolean {
  return value === null || typeof value === "string" || typeof value === "number" || typeof value === "boolean";
}

function renderScalar(value: unknown): string {
  if (value === null) return "null";
  if (typeof value === "number" || typeof value === "boolean") return String(value);
  const text = String(value);
  const mustQuote = /[#&*{}\[\],]/.test(text);
  if (!mustQuote && /^[A-Za-z0-9_./:@?=+ -]+$/.test(text) && !/^(true|false|null|~|-|\d+(\.\d+)?)$/i.test(text) && !text.includes(": ")) {
    return text;
  }
  return JSON.stringify(text);
}

function key(value: string): string {
  return /^[A-Za-z0-9_$.-]+$/.test(value) ? value : JSON.stringify(value);
}

function toYaml(value: unknown, indent = 0): string {
  const pad = " ".repeat(indent);
  if (isScalar(value)) return `${pad}${renderScalar(value)}`;
  if (Array.isArray(value)) {
    if (value.length === 0) return `${pad}[]`;
    return value.map((item) => {
      if (isScalar(item)) return `${pad}- ${renderScalar(item)}`;
      const rendered = toYaml(item, indent + 2);
      return `${pad}-\n${rendered}`;
    }).join("\n");
  }
  if (value && typeof value === "object") {
    const entries = Object.entries(value as Record<string, unknown>).filter(([, entryValue]) => entryValue !== undefined);
    if (entries.length === 0) return `${pad}{}`;
    return entries.map(([entryKey, entryValue]) => {
      if (isScalar(entryValue)) return `${pad}${key(entryKey)}: ${renderScalar(entryValue)}`;
      if (Array.isArray(entryValue) && entryValue.length === 0) return `${pad}${key(entryKey)}: []`;
      if (entryValue && typeof entryValue === "object" && !Array.isArray(entryValue) && Object.keys(entryValue as Record<string, unknown>).length === 0) {
        return `${pad}${key(entryKey)}: {}`;
      }
      return `${pad}${key(entryKey)}:\n${toYaml(entryValue, indent + 2)}`;
    }).join("\n");
  }
  return `${pad}${renderScalar(String(value))}`;
}

async function main(): Promise<void> {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    console.log(usage());
    return;
  }

  const runDir = await resolveRunDir(args);
  const featureCoverage = await readJson<FeatureCoverage>(join(runDir, "feature-coverage.json"));
  const schemaCoverage = await readJson<SchemaCoverage>(join(runDir, "schema-coverage.json"));
  const projectName = args.projectName ?? featureCoverage.projectName ?? "project";
  const ops = operations();
  const openapi = buildOpenApi(projectName, ops, featureCoverage);
  const coverage = buildCoverage(projectName, ops, featureCoverage, schemaCoverage);
  const markdown = renderMarkdown(projectName, ops, coverage, featureCoverage);
  const workDir = join(runDir, RUN_WORK_DIR);

  await mkdir(workDir, { recursive: true });
  await writeFile(join(runDir, "api-definition.openapi.json"), JSON.stringify(openapi, null, 2) + "\n", "utf8");
  await writeFile(join(runDir, "api-definition.openapi.yaml"), toYaml(openapi) + "\n", "utf8");
  await writeFile(join(runDir, "api-coverage.json"), JSON.stringify(coverage, null, 2) + "\n", "utf8");
  await writeFile(join(runDir, "api-definition.md"), markdown + "\n", "utf8");
  await writeFile(join(workDir, "api-generation-summary.json"), JSON.stringify({
    projectName,
    apiCount: ops.length,
    pathCount: Object.keys(openapi.paths as Record<string, unknown>).length,
    selectedAreas: featureCoverage.selectedAreas ?? [],
    extraContext: featureCoverage.extraContext ?? "",
  }, null, 2) + "\n", "utf8");

  console.log(JSON.stringify({
    ok: true,
    projectName,
    runDir,
    apiCount: ops.length,
    openapiYaml: join(runDir, "api-definition.openapi.yaml"),
    openapiJson: join(runDir, "api-definition.openapi.json"),
    apiDefinition: join(runDir, "api-definition.md"),
    apiCoverage: join(runDir, "api-coverage.json"),
  }, null, 2));
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});
