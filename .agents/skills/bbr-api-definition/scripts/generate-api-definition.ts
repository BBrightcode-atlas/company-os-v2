function featureLabel(featureCoverage: FeatureCoverage, featureId: string): string {
  const feature = featureCoverage.features.find((item) => item.featureId === featureId);
  return feature ? `${feature.featureId} ${feature.featureName}` : featureId;
}

function featureTagsForOperation(featureCoverage: FeatureCoverage, item: OperationDef): string[] {
  return item.featureCodes.map((featureId) => featureLabel(featureCoverage, featureId));
}

